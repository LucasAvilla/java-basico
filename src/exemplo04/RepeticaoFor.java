package exemplo04;

/**
 * Programa par ademonstrar o comando de repetição FOR
 * 
 * @author Lucas Igor
 * @since 10/02/2021
 */

public class RepeticaoFor {

	/*
	 * Método principal para executar a classe
	 */
	public static void main(String[] args) {

		// Declarando as variáveis

		// for (crio e inicializo; condição; incremento)

		for (int i = 0; i < 10; i++) {// inicio do laço
			System.out.println(i + "ao cubo é: " + ((i * i) * i));

		} // fim do laço

	}

}
