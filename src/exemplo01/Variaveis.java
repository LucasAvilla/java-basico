package exemplo01;

/**
 * Programa para demonstrar a utilização de variaveis
 * 
 * @author lavilla
 * @since 04/02/2021
 */
public class Variaveis {

	/*
	 * Método principal para executar o programa
	 */
	public static void main(String[] args) {

		// Declarando uma variavel do tipo inteiro
		int idade;
		// Declarando uma variavel do tipo String
		String nome;
		// Declarando uma variavel do tipo double
		double valor;

		// Atribuindo valores as variáveis
		idade = 37;
		nome = "Diego Munhoz";
		valor = 155.98;

		// Exibindo o conteudo das variáveis no console
		System.out.println("Variavel IDADE: " + idade + ".");
		System.out.println("Variavel NOME: " + nome + ".");
		System.out.println("Variavel VALOR:" + valor + ".");

	}
	
	
}
