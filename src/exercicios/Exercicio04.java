package exercicios;

/**
 * Programa para declarar variáveis do tipo String chamadas Nome, Celular,
 * Cidade e stribuir valores para exibir no console
 * 
 * @author Lucas Igor A M Nogueira
 * @since 04/02/2021
 */
public class Exercicio04 {

	/*
	 * Método para executar o programa
	 */
	public static void main(String[] args) {

		// Declarando variáveis do tipo String
		String nome, celular, cidade;

		// Atribuindo valores as variáveis
		nome = "Lucas Igor";
		celular = "(41) 98781-2653";
		cidade = "Curitiba";

		// Exibindo o conteudo das variáveis no console
		System.out.println("Variável Nome: " + nome + ".");
		System.out.println("Variável celular: " + celular + ".");
		System.out.println("Variável cidade: " + cidade + ".");

	}

}
