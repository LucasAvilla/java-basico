package exercicios;

/**
 * Programa para declarar as variáveis do tipo String: Nome, Celular e Cidade.
 * Atribuir valores e exibir no console.
 * 
 * @author Lucas Igor A M Nogueira
 * @since 04/02/2021
 */
public class Exercicio05 {

	/*
	 * Método para executar o programa
	 */

	public static void main(String[] args) {

		// Declarando variáveis do tipo inteiro
		int idade;
		double altura, peso;

		// Atribuindo valores as variáveis
		idade = 34;
		altura = 1.78;
		peso = 94.00;

		// Exibindo o conteudo das variáveis no console
		System.out.println("Variável Idade: " + idade + " anos.");
		System.out.println("Variável Altura: " + altura + " m.");
		System.out.println("Variável Peso: " + peso + " Kg.");

	}

}
